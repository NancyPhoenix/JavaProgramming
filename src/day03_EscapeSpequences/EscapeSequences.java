package day03_EscapeSpequences;
/*
escape sequences:must be given double quote
\n:starts new line
\t:tab button,paragraph space
\\:single backward slash
\":double quote


 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \npython \nC#");
        System.out.println("------------------------------------" );

        System.out.println("hello cydeo \nhow are you all today \nnice to see you \nyou are welcome");
        System.out.println("-------------------------------------");
        System.out.println("\tJava is cool programming language");
        System.out.println("-----------------------------------");

        System.out.println("/ \\");
        System.out.println("my favorite TV show is \"Friends \"");




    }




}
